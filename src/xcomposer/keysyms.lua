-- According to my tests with xdotool, some of the higher keysyms may not be
-- accurate. It would print the lowercase 'ň' when using 'Ncaron', for example.
-- Because of this, codepoints should be preferred over keysyms.


local function format(rawdata)
  local keysyms = {}
 
  for i, raw in ipairs(rawdata) do
    local sym = {
        code = raw[1],
        str  = raw[2],
        live = raw[3],
        dead = raw[4],
    }
    keysyms[sym.str] = sym
  end
 
  return keysyms
end


